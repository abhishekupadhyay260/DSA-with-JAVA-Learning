public class HelloWorld {
    public static void main (String args[]){
//        this is "main block" where we write our code.
//   we use curly brackets ({}) to define from where the code started (by this - { ) and where it ends (by this - } )
        System.out.print("Hello World");
//        here "System" --> is file which has code,
//        "out" --> is to give output,
//                "print" --> is for printing the output
//        "Hello World" --> is content which we are printing
//        (" ") is because we are printing a string, for other objects just write object
//        semicolon ";" is used to terminate the line
        System.out.print("Hello World\n");
//        this "\n" will indicate to print the next term in new line
        System.out.println("Hello World");
//        println also print next term in new line


    }
}

//this app will show output on console,
//Console is area from where, we take input from user or/and give output to user